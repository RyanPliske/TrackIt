import Foundation

extension UIColor {
    class func TRBabyBlue() -> UIColor {
        return UIColor(red: (0.0 / 255.0), green: (147.0 / 255.0), blue: (255.0 / 255.0), alpha: 1.0)
    }
    
    class func TRAquaBlue() -> UIColor {
        return UIColor(red: 0.0, green: 147.0, blue: 255.0, alpha: 1.0)
    }
}